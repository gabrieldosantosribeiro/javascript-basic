// Months Function: Write a function that converts a number (1-12) into its respective month name.

function returMonth(n) {
    switch (n) {
        case 1:
            return 'january'
            break
        case 2:
            return 'february'
            break
        case 3:
            return 'march'
            break
        case 4:
            return 'april'
            break
        case 5:
            return 'may'
            break
        case 6:
            return 'june'
            break
        case 7:
            return 'july'
            break
        case 8:
            return 'august'
            break
        case 9:
            return 'september'
            break
        case 10:
            return 'october'
            break
        case 11:
            return 'november'
            break
        case 12:
            return 'december'
        default:
            break
    }
}